package lab_numbers;

public class QuotientAndRemainder {

	public static void main(String[] args) {
		int number1= 150, number2= 20, quotient, remainder;
		quotient=number1/number2; //150/(divide)20=7
		remainder= number1%number2;  //150%(module)20=10
		System.out.println("quotient are: "+quotient);
		System.out.println("remainder are: "+remainder);
	}

}
